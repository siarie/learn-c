/*
 * Learn C: Variable
 *
 * Learn how to declare 
 * and initialize variable.
 */
#include <stdio.h>


int main(void) {
	int age; // declaration
	age = 21; // initialize
	printf("Age = %d\n", age);

	// declare and initialize
	float height = 179.4;
	height = 180; // change value of height
	printf("Height = %f\n", height);
}
